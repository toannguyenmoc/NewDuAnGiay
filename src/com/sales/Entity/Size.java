package com.sales.Entity;

/**
 *
 * @author GF63
 */
public class Size {
    private int id;
    private String name;
    private Boolean active;

    public Size() {
    }

    public Size(int id, String name, Boolean active) {
        this.id = id;
        this.name = name;
        this.active = active;
    }

    public Boolean getActive() {
        return active;
    }

    public void setActive(Boolean active) {
        this.active = active;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
