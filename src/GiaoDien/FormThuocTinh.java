    void setModelColor(Color model) {

        txtMauSac.setText(String.valueOf(model.getName()));
        if (model.getActive()) {
            cknHoatDong.setSelected(true);
        } else {
            cknNgungHoatDong.setSelected(true);
        }

    }

    void setModelSize(Size model) {

        txtSize.setText(String.valueOf(model.getName()));
        if (model.getActive()) {
            cknHoatDongKichThuoc.setSelected(true);
        } else {
            cknNgungHoatDongKickThuoc.setSelected(true);
        }

    }

