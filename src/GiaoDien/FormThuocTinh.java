    public void searchColor() {
        // DefaultTableModel model = (DefaultTableModel) tblMauSac.getModel();
        modelColor.setRowCount(0);
        //model.setRowCount(0);
        List<Color> list = colorDao.selectByObject(txtTimKiemMauSac.getText());
        if (list != null) {
            for (Color color : list) {
                Object[] row = {
                    color.getId(),
                    color.getName(),
                    color.getActive() ? "Hoạt Động" : "Ngừng Hoạt Động"
                };
                modelColor.addRow(row);
            }
            tblMauSac.setModel(modelColor);
        }
    }

